/*
找出1到99之间的全部同构数。
若某数出现在其平方数的右边则称该数为同构数。
例如，5是25右边的数，25是625右边的数，则5和25都是同构数。
*/

# include <stdio.h>
int main()
{
	int i;//控制1-99 
	printf("1-99之间的同构数有：") ;
	for (i=1;i<100;i++)
	{
		if (i%10!=1&&i%10!=5&&i%10!=6)//首先从个位排除 
			continue;
		if (i<10)//决定之后%10还是%100 
		{
			if (i*i%10==i)
			{
				printf("%d ",i);
			}
		}
		else if (i>=10)
		{
			if (i*i%100==i)
			{
				printf("%d ",i);
			}
		}
	}
	return 0;
}

